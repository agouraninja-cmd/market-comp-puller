        const cacheKey = cacheKeyFor({
          address: addressOk, type: typeOk, note: noteOk, months: monthsOk,
          maxComps: maxCompsOk, txFocus: txFocusOk, subjectSizeSqft: sizeOk, verifiedComps,
        });

